def query_openrouter_api(prompt):
    """–û—Ç–ø—Ä–∞–≤–ª—è–µ—Ç –∑–∞–ø—Ä–æ—Å –≤ OpenRouter API —Å –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ–º Qwen 2.5"""
    try:
        logger.info(f"–ó–∞–ø—Ä–æ—Å –∫ OpenRouter API: {prompt[:100]}...")
        
        url = "https://openrouter.ai/api/v1/chat/completions"
        
        # –ò—Å–ø–æ–ª—å–∑—É–µ–º –∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–π –∏–¥–µ–Ω—Ç–∏—Ñ–∏–∫–∞—Ç–æ—Ä Qwen 2.5
        model_id = "qwen/qwen2.5-72b-chat"
        
        payload = {
            "model": model_id,
            "messages": [
                {
                    "role": "system",
                    "content": "–¢—ã –ø–æ–ª–µ–∑–Ω—ã–π –ø–æ–º–æ—â–Ω–∏–∫ –¥–ª—è —Å—Ç—É–¥–µ–Ω—Ç–æ–≤ –∏ —à–∫–æ–ª—å–Ω–∏–∫–æ–≤. –û—Ç–≤–µ—á–∞–π —á–µ—Ç–∫–æ, –ø–æ –¥–µ–ª—É –∏ –Ω–∞ —Ä—É—Å—Å–∫–æ–º —è–∑—ã–∫–µ. –ï—Å–ª–∏ –Ω–µ –∑–Ω–∞–µ—à—å —Ç–æ—á–Ω–æ–≥–æ –æ—Ç–≤–µ—Ç–∞, —Å–∫–∞–∂–∏ –æ–± —ç—Ç–æ–º."
                },
                {
                    "role": "user",
                    "content": prompt
                }
            ],
            "temperature": 0.7,
            "max_tokens": 2000,
            "frequency_penalty": 0.2,
            "presence_penalty": 0.2
        }
        
        response = requests.post(url, headers=OPENROUTER_HEADERS, json=payload, timeout=60)
        logger.info(f"OpenRouter API status: {response.status_code}")
        
        if response.status_code == 200:
            data = response.json()
            answer = data['choices'][0]['message']['content'].strip()
            logger.info(f"–ü–æ–ª—É—á–µ–Ω –æ—Ç–≤–µ—Ç –æ—Ç OpenRouter API: {len(answer)} —Å–∏–º–≤–æ–ª–æ–≤")
            return answer
        else:
            # –î–µ—Ç–∞–ª—å–Ω—ã–π –∞–Ω–∞–ª–∏–∑ –æ—à–∏–±–∫–∏
            try:
                error_data = response.json()
                error_info = error_data.get('error', {})
                error_code = error_info.get('code', 'UNKNOWN')
                error_message = error_info.get('message', '–ë–µ–∑ –æ–ø–∏—Å–∞–Ω–∏—è')
                
                logger.error(f"OpenRouter API error {response.status_code}: [{error_code}] {error_message}")
                
                # –§–æ—Ä–º–∏—Ä—É–µ–º –ø–æ–Ω—è—Ç–Ω–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ –æ–± –æ—à–∏–±–∫–µ
                if response.status_code == 400:
                    return f"‚ùå –û—à–∏–±–∫–∞ –∑–∞–ø—Ä–æ—Å–∞ –∫ –ò–ò: {error_message}"
                elif response.status_code == 401:
                    return "‚ùå –û—à–∏–±–∫–∞ –∞–≤—Ç–æ—Ä–∏–∑–∞—Ü–∏–∏ OpenRouter API. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ —Ç–æ–∫–µ–Ω."
                elif response.status_code == 403:
                    return "‚ùå –î–æ—Å—Ç—É–ø –∫ OpenRouter API –∑–∞–ø—Ä–µ—â–µ–Ω. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ —Ç–æ–∫–µ–Ω –∏ –æ–≥—Ä–∞–Ω–∏—á–µ–Ω–∏—è."
                elif response.status_code == 429:
                    return "‚è∞ –ü—Ä–µ–≤—ã—à–µ–Ω –ª–∏–º–∏—Ç –∑–∞–ø—Ä–æ—Å–æ–≤ –∫ OpenRouter API. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –ø–æ–∑–∂–µ."
                else:
                    return f"‚ùå –û—à–∏–±–∫–∞ OpenRouter API: {response.status_code} - {error_code}"
            except json.JSONDecodeError:
                logger.error(f"OpenRouter API –≤–µ—Ä–Ω—É–ª –Ω–µ–≤–∞–ª–∏–¥–Ω—ã–π JSON: {response.text[:200]}")
                return f"‚ùå –û—à–∏–±–∫–∞ OpenRouter API: {response.status_code}"
            
    except requests.exceptions.Timeout:
        logger.error("–¢–∞–π–º–∞—É—Ç –ø—Ä–∏ –∑–∞–ø—Ä–æ—Å–µ –∫ OpenRouter API")
        return "‚åõ –¢–∞–π–º–∞—É—Ç —Å–æ–µ–¥–∏–Ω–µ–Ω–∏—è —Å –ò–ò-—Å–µ—Ä–≤–∏—Å–æ–º"
    except requests.exceptions.ConnectionError:
        logger.error("–û—à–∏–±–∫–∞ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è –∫ OpenRouter API")
        return "üîå –û—à–∏–±–∫–∞ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è –∫ –ò–ò-—Å–µ—Ä–≤–∏—Å—É"
    except Exception as e:
        logger.error(f"–û—à–∏–±–∫–∞ –∑–∞–ø—Ä–æ—Å–∞ –∫ OpenRouter API: {str(e)}")
        return f"‚ö†Ô∏è –ù–µ–ø—Ä–µ–¥–≤–∏–¥–µ–Ω–Ω–∞—è –æ—à–∏–±–∫–∞: {str(e)}"

def check_model_availability():
    """–ü—Ä–æ–≤–µ—Ä—è–µ—Ç –¥–æ—Å—Ç—É–ø–Ω–æ—Å—Ç—å –º–æ–¥–µ–ª–∏ –Ω–∞ OpenRouter"""
